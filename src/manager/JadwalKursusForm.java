    public void setTabel() {
        tableModel.clearData();
        try {
            int count = 0;
            Statement sta = this.cont.getStatement();
            String query = "select COUNT(idJadwal) as jumlah from Jadwal";
            ResultSet rs = sta.executeQuery(query);
            rs.next();
            int size = rs.getInt("jumlah");
            String[][] input = new String[size][5];
            query = "select * from Jadwal order by idPaket asc";
            rs = sta.executeQuery(query);
            while (rs.next()) {
                input[count][0] = rs.getString("idJadwal");
                input[count][1] = rs.getString("idPaket");
                input[count][2] = rs.getString("hari");
                input[count][3] = rs.getString("jamMulai");
                input[count][4] = rs.getString("jamAkhir");
                count++;
            }
            tableModel.updateData(input, size);
        } catch (SQLException ex) {
            Logger.getLogger(LaporanDataPegawaiForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

