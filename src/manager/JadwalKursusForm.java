    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int index = tabelJadwalKursus.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(null, "Row is not selected.");
        } else {
            int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this information?", "Choose", JOptionPane.YES_NO_OPTION);
            if (confirm == 0) {
                try {
                    Statement sta = this.cont.getStatement();
                    String idJadwal = tabelJadwalKursus.getValueAt(index, 0) + "";
                    String query = String.format("select idSiswa from Siswa where idJadwal = '%s'", idJadwal);
                    ResultSet rs = sta.executeQuery(query);
                    Queue queue = new LinkedList();
                    String idSiswa;
                    while(rs.next()){
                        queue.add(rs.getString("idSiswa"));
                    }
                    while (!queue.isEmpty()) {
                        idSiswa = queue.poll()+"";
                        query = String.format("delete from Transaksi where idSiswa = '%s'", idSiswa);
                        sta.execute(query);
                        query = String.format("delete from PelaksanaanKursus where idSiswa = '%s'", idSiswa);
                        sta.execute(query);
                        query = String.format("delete from DaftarHadir where idSiswa = '%s'", idSiswa);
                        sta.execute(query);
                        query = String.format("delete from Siswa where idSiswa = '%s'", idSiswa);
                        sta.execute(query);
                    }
                    query = String.format("delete from Jadwal where idJadwal = '%s'", idJadwal);
                    boolean exe = sta.execute(query);
                    if (!exe) {
                        JOptionPane.showMessageDialog(null, "Successful.");
                    } else {
                        JOptionPane.showMessageDialog(null, "Delete unsuccessful.");
                    }
                    this.setTabel();
                } catch (SQLException ex) {
                    Logger.getLogger(LaporanDataPegawaiForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

