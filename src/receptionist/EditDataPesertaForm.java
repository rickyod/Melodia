    public void setData(String idSiswa){
        String query = String.format("select * from Siswa where idSiswa = '%s'",idSiswa);
        try {
            ResultSet rs = statement.executeQuery(query);
            rs.next();
            this.idSiswa = idSiswa;
            this.idSiswaLabel.setText(idSiswa);
            namaLengkap = rs.getString("namaLengkap");
            this.namaLengkapField.setText(namaLengkap);
            tglLahir = rs.getString("tglLahir");
            String[] split = tglLahir.split("-");
            tahun = split[0];
            bulan = Integer.parseInt(split[1])+"";
            tanggal = Integer.parseInt(split[2])+"";
            tahunBox.setSelectedItem(tahun);
            bulanBox.setSelectedItem(bulan);
            tanggalBox.setSelectedItem(tanggal);
            alamat = rs.getString("alamat");
            this.alamatField.setText(alamat);
            nomorHp = rs.getString("nomorHp");
            this.nomorHpField.setText(nomorHp);
        } catch (SQLException ex) {
            Logger.getLogger(EditDataPesertaForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

