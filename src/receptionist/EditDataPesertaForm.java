    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        boolean isChanges = false;
        String newNama = namaLengkapField.getText().trim();
        String query;
        ResultSet rs;
        if(!newNama.equals(namaLengkap)){
            isChanges = true;
            query = String.format("UPDATE Siswa SET namaLengkap = '%s' where idSiswa ='%s' " , newNama , idSiswa);
            try {
                statement.execute(query);
            } catch (SQLException ex) {
                Logger.getLogger(EditDataPesertaForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        String newTanggalLahir = tahunBox.getSelectedItem()+"-"+bulanBox.getSelectedItem()+"-"+tanggalBox.getSelectedItem();
        if (!newTanggalLahir.equals(tglLahir)) {
            isChanges = true;
            query = String.format("UPDATE Siswa SET tglLahir = '%s' where idSiswa ='%s' " , newTanggalLahir , idSiswa);
            try {
                statement.execute(query);
            } catch (SQLException ex) {
                Logger.getLogger(EditDataPesertaForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        String newAlamat = alamatField.getText().trim();
        if(!newAlamat.equals(alamat)){
            isChanges = true;
            query = String.format("UPDATE Siswa SET alamat = '%s' where idSiswa ='%s' " , newAlamat , idSiswa);
            try {
                statement.execute(query);
            } catch (SQLException ex) {
                Logger.getLogger(EditDataPesertaForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        String newNomorHp = nomorHpField.getText().trim();
        if(!newNomorHp.equals(nomorHp)){
            isChanges = true;
            query = String.format("UPDATE Siswa SET nomorHp = '%s' where idSiswa ='%s' " , newNomorHp , idSiswa);
            try {
                statement.execute(query);
            } catch (SQLException ex) {
                Logger.getLogger(EditDataPesertaForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        if(isChanges){
            JOptionPane.showMessageDialog(null, "Changes saved.");
        }
        this.setVisible(false);
        this.cont.getDataPeserta().setTabel();
        this.cont.getDataPeserta().setVisible(true);
    }//GEN-LAST:event_saveButtonActionPerformed

