    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        try {
            String tanggal = this.tanggalLabel.getText();
            String idPeserta = this.idPesertaField.getText().trim();
            String query = String.format("select sisaPertemuan from Siswa where idSiswa = '%s'", idPeserta);
            ResultSet rs = statement.executeQuery(query);
            int sisaPertemuan = 0;
            while (rs.next()) {
                sisaPertemuan = rs.getInt("sisaPertemuan");
            }
            if (sisaPertemuan == 0) {
                JOptionPane.showMessageDialog(null, "Sisa pertemuan telah habis , silahkan daftar ulang.");
            } else {
                sisaPertemuan--;
                query = String.format("UPDATE Siswa SET sisaPertemuan = '%s' WHERE idSiswa = '%s'",sisaPertemuan,idPeserta);
                statement.execute(query);
                query = String.format("UPDATE DaftarHadir SET statusKehadiran ='true' where idSiswa = '%s' and tanggal = '%s'", idPeserta, tanggal);
                boolean exe = statement.execute(query);
                String jam = new SimpleDateFormat("HH:mm:ss").format(Calendar.getInstance().getTime());
                query = String.format("INSERT INTO PelaksanaanKursus values('%s','%s','%s','%d')", idPeserta, tanggal, jam, sisaPertemuan);
                statement.execute(query);
                int confirm = JOptionPane.showConfirmDialog(null, "Berhasil", "Choose", JOptionPane.YES_OPTION);
                if(confirm==0){
                    this.cont.getDataKehadiran().setVisible(true);
                    this.setVisible(false);
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateKehadiranForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateButtonActionPerformed

