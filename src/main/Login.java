    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        this.id = this.usernameField.getText();
        this.password = this.jPasswordField.getPassword();
        this.jPasswordField.setText("");
        try {
            String idPegawai = "";
            String idJabatan = "";
            String jabatan = "";
            String passwordChecker;
            char[] passArr;
            //checking password
            Statement sta = this.cont.getStatement();
            String query = String.format("select * from Account where username='%s'", id);
            ResultSet rs = sta.executeQuery(query);
            rs.next();
            passwordChecker = rs.getString("password");
            passArr = passwordChecker.toCharArray();
            if (Arrays.equals(passArr, this.password)) {
                idPegawai = rs.getString("idPegawai"); //mendapatkan idPegawai
                query = String.format("select\n"
                        + "	*\n"
                        + "from\n"
                        + "	DataPegawai inner join Jabatan\n"
                        + "on	\n"
                        + "	DataPegawai.jabatan = Jabatan.idJabatan\n"
                        + "where\n"
                        + "	idPegawai = '%s'", idPegawai); //mencari pegawai 
                rs = sta.executeQuery(query);
                rs.next();
                jabatan = rs.getString("NamaJabatan");
                this.cont.setLoggedIn(jabatan);
                this.cont.setIdPegawai(idPegawai);
                String namaPegawai = rs.getString("namaPegawai");
                this.cont.setNamaPegawai(namaPegawai);
                if (jabatan.equals("Manager")) {
                    cont.getHomepageManager().setVisible(true);
                } else {
                    cont.getHomepageRec().setVisible(true);
                }
                this.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "Username atau password salah.");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "An error occured.");
        }

    }//GEN-LAST:event_loginButtonActionPerformed


